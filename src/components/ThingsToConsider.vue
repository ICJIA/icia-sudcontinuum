          <p>In any type of program, practice, or initiative for individuals with SUDs, it is imperative that they are evidence-informed, meaning that rigorous evaluation and research proves the practices and program components have produced significant, replicable positive outcomes. Implementing these programs and practices with a high degree of fidelity can help improve and save lives as well as improve public safety and decrease taxpayer and criminal justice system expenses.<a href="#fn18" id="fnref18" title="See footnote" class="pop-intro light">18</a>  In fact, for every <span>$</span>1 spent on SUD treatment, there is in an estimated <span>$</span>4 to <span>$</span>7 return in the form of reduced drug-related crime, and criminal justice costs, and health care cost savings.<a href="#fn19" id="fnref19" title="See footnote" class="pop-intro light">19</a>  Criminal justice professionals, in conjunction with other policymakers, stakeholders, and providers, can be more effective at reducing recidivism and substance use with limited resources when they use initiatives that have been shown to work. This continuum outlined several ways in which schools, communities, families, law enforcement, courts and supervision, and correctional institutions can refer, divert, and treat individuals with SUDs, based on evidence-informed practices and offered research, resources, and examples.</p>

